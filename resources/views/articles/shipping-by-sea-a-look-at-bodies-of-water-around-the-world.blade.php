                <h2>Shipping by Sea <br/> A Look at Bodies of Water Around the World</h2>

                <p>Trade is the transfer of goods and services from one person, company, or country to another by the
                    exchange of equal or higher value. Countries refer to trade as commerce, or a financial transaction
                    that consists of exchanging goods and services most commonly in demand. The original form of trade
                    occurred by exchanging metals, coins, bills, paper money, animals, textiles, and weaponry. The
                    original form of trade occurred on foot, or by animal transportation. The sea was later used to
                    transport goods and services. In today's world, trade occurs by air, train, truck, or by sea.</p>

