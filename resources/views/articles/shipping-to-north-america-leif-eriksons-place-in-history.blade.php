                <h2>Shipping to North America - Leif Erikson's Place in History</h2>

                <p>Leif Erikson, also known as Leif Eriksson, Leif Ericson, or Leif the Lucky, was the son of Eric the
                    Red. Eric the Red was a Viking who was exiled from Norway and Iceland. As he journeyed from his
                    home, he discovered a country that he named Greenland. It is in Greenland where he and his family
                    eventually settled. Leif was born on or around 975 A.D., although the exact date of his birth is
                    uncertain and often varies according to different accounts. Sixteen years after moving to Greenland,
                    while in his twenties, Leif traveled on his own back to Norway. Upon his arrival, Leif visited King
                    Olaf. </p>
                <p>It was King Olaf who spoke to Leif about Christianity and eventually converted and baptized
                    Leif and his crew. Olaf then sent Leif back to Greenland with instructions to spread
                    Christianity.</p>
                <p>There are two different accounts on how Eriksson arrived in North America.
                    These sagas are known as the Eiriks Saga and the Groenlendinga Saga. The Eiriks saga claims that
                    Leif was returning to his home in Greenland from Norway, when a storm caused him to sail off-course.
                    He arrived along the coast of new lands which he eventually named Vinland due to the abundance of
                    grapes there.</p>
                <p>In the Groenlendinga saga, a trader, Bjarni Herjulfsson, spoke of unknown lands that
                    he had seen, but not set foot on during his travels. After hearing these tales, Leif purchased a
                    ship and gathered a crew of 35 men. According to this saga, he intentionally traveled to the lands
                    that Bjarni spoke of. Once there he landed in several locations. One location he called Markland,
                    which is believed to be Newfoundland, and the other he named Vinland. </p>
                <p>It was not until recent times
                    that archaeologists discovered where they believe is the location of Markland and Vineland, which is
                    thought to be in Canada. As a result of his journey, history has come to recognize Leif as the first
                    European to set foot in North America.
                <p></p>
                <h3>Leif Erikson versus Christopher Columbus</h3>
                <p>For
                    most people, they believe Christopher Columbus was the first European explorer to discover North
                    America. However, nearly 500 years prior to Columbus, Leif and his crew of 35 traveled from his home
                    to reach what he named Vinland. Originally these were sagas, or tales; however, in the 1960s
                    evidence was discovered which seemed to confirm these sagas as truth. The evidence was in the form
                    of artifacts and remnants of homes similar to Icelandic homes. Unlike Columbus who first landed in
                    South America first, it is believed that Leif's arrival in what he referred to as Vineland, was in
                    Canada.</p>

                <h3>Leif's Legacy Around the World</h3>
                <p>In modern society, Erikson is widely
                    considered a legend and a brave adventurer. Around the world, there have been numerous statues
                    raised in his honor. In Iceland, there stands a statue of the explorer. In 2000, on the 1000th
                    anniversary of his expedition, a statue of his likeness was erected in Southern Greenland. In the
                    United States, there are several statues in his honor located around the country, including Seattle
                    and Minnesota. His popularity his highest with those of Nordic ancestry.<br/></p>

                <h3>Leif Erikson Day in America</h3>

                <p>In the United States, Americans observe this Nordic
                    explorer and his accomplishment on October 9. In September 1964, Lyndon B. Johnson proclaimed
                    October 9 as Leif Eriksson Day across the country. Prior to this proclamation, the day had been
                    observed by a handful of states, such as Wisconsin, Minnesota, South Dakota, and Illinois. The
                    date is not meant to be reflective of when Eriksson arrived in the U.S. It is meant instead to
                    stand for the day in 1825 when immigrants from Norway first ventured to the U.S. as an organized
                    group.</p>
