                        ships that have a specific regional significance. Organizations dedicated to preserving historic
                        ships, like the Historic Naval Ships Association, serve as valuable starting points for model
                        builders who want to accurately recreate a specific type of ship. The Nautical Research Guild
                        provides a comprehensive list of resources covering a variety of model ship building
