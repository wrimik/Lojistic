                    <h3>The History of Model Ships </h3>

                    <p>Model ships served both practical and symbolic functions in early sea-faring cultures. Ancient
                        Mediterranean people relied heavily on water transportation as a means of supporting their
                        ever-expanding empires, and their fascination with ships is well documented in the archeological
                        record. Numerous model ships called "solar boats", believed to be symbolic representations of
                        the soul's journey into the afterlife, have been unearthed in tombs across Egypt. These
                        small-scale model ships provide archeologists with an idea of what large-scale, functional ships
                        may have looked like during the time of the Pharaohs.</p>
