                    <h3>Uses of Model Ships </h3>

                    <p>Building full-scale ships to test new, and unproven, design innovations is both costly and
                        impractical. Model ships are often used as templates for testing large-scale designs, and in
                        simulations of situations that would be impossible to recreate in the real-world. The cargo and
                        container ship industry relies on laboratory simulations to improve the safety and effectiveness
                        of their ship's designs. In an activity known as "wargaming", military strategists and
                        historians use model ships to recreate important battles.</p>
