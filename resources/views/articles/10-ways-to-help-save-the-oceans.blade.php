                    <hr/>
                </p>

                <h3>Educate Yourself </h3>
                <p>One of the best ways to know how to save the oceans is to learn more about it.
                    Read up on the different ways in which they are being harmed, and then research
                    ways to reduce the impact. Educating ourselves about these issues goes much further
                    than only learning about the ocean. Find out about how the ocean and its marine life
                    are connected to all of us and our economy. Teach your friends and family too, so that
                    everyone can be more aware.
                </p>

                <p>
                    <a rel="nofollow" href="http://www.nature.org/ourinitiatives/habitats/oceanscoasts/explore/five-reasons-we-are-all-connected-to-oceans.xml" target="_blank">
