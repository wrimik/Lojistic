                </p>

                <h3>Don&rsquo;t Litter at the Beach</h3>
                <p>Reducing litter at the beach is one of the biggest ways to lower the impact of
                    ocean pollution. Take a garbage bag along to collect litter produced during your
                    trip. Afterwards, deposit it in a trashcan, or take it home to dispose of it
                    there. To make an even bigger difference, join or organize a beach clean-up mission.
                </p>

                <p>
                    <a rel="nofollow" href="http://www.whitman.edu/environmental_studies/internships/reports/coggan.htm" target="_blank">
