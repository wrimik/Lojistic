                </p>

                <h3>Boat and Fish Responsibly</h3>
                <p>
                    A good deal of ocean pollution comes from boaters and fishers who dump trash and
                    chemical products into the water. When fishing, obey the local fishing laws and
                    never fish above the legal limits. Be respectful of natural habitats; collect
                    any trash in a bag and dispose of it properly upon returning back to shore. Boaters
                    should maintain their boats regularly to avoid accidental spills of fuels or other
                    hazardous materials.
                </p>

                <p>
                    <a rel="nofollow" href="http://www.mass.gov/dfwele/dmf/spotlight/fish_responsibly.htm" target="_blank">
