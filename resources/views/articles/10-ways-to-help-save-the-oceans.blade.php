                <h2>10 Ways To Help Save The Oceans by Lojistic</h2>
                <p>
                    It is easy to forget about keeping our oceans clean. They are such large,
                    vast bodies of water that it seems incredible that a single plastic bottle
                    could really affect its biodiversity. In truth, when everyone thinks this
                    way, it really does add up. Apart from trash, our oceans suffer in many other
                    ways too. Pollution from toxic chemicals, fertilizers, petroleum, and other
                    substances harms life in the ocean. Since we are all connected and dependent
                    on the ocean as a food source, as well as an important element of climate,
                    it is in our best interest to keep the water clean. Read on for some tips on
                    how to get started!
                </p>
                <h3>Reduce Carbon Footprints</h3>
                <p>
                    When we use fossil fuels for electricity and energy, it creates extra carbon
                    dioxide in the atmosphere. When this extra carbon dioxide is absorbed into oceans,
                    it makes the water more acidic. In turn, the higher acidity levels negatively
                    affect ocean creatures and plants. By paying attention to our energy use and
                    lowering it, we can contribute towards a mass effort in maintaining a lower acidity
                    level in the waters.
                </p>
                <p>
                    <a rel="nofollow" href="http://sustainability.georgetown.edu/getinvolved/thingsyoucando/" target="_blank">
                        Ways to Reduce Carbon Footprints
                    </a>
                    <br/>
                    <a rel="nofollow" href="http://edis.ifas.ufl.edu/document_fy1040" target="_blank">
                        Energy Efficiency for a Lower Carbon Footprint
                    </a>
                    <hr/>
                </p>

                <h3>Consume Sustainable Seafood Products</h3>
                <p>
                    Governments have set fishing laws in place in order to help control fishing
                    practices and allow ocean life to continue to thrive naturally. When companies
                    disregard these laws and overfish, marine populations are depleted faster than
                    they can recover. We can play our part in discouraging these practices by only
                    buying from companies that use sustainable fishing practices. Always read the
                    labels to check for sustainability information.
                </p>
                <p>
                    <a rel="nofollow" href="http://seafood.ucdavis.edu/consumer/NFI%20Fact%20Sheet%20on%20Seafood%20Sustainability.pdf" target="_blank">
                        Seafood Sustainability Facts
                    </a>
                    <br/>
                    <a rel="nofollow" href="http://www.fishwatch.gov/" target="_blank">
                        Learn All About Sustainable Seafood
                    </a>
                    <hr/>
                </p>

                <h3>Use Less Plastic</h3>
                <p>
                    Plastic can eventually wind up in the ocean in many ways, even if beach-goers
                    do not directly dump it there. Items like plastic bottles, bags, and soda can
                    plastic rings can suffocate or entangle marine creatures. Thousands of them die
                    this way each year. Limit your plastic usage by opting for reusable items instead.
                    When using plastic, make sure to recycle it properly.
                </p>
                <p>
                    <a rel="nofollow" href="http://www.whoi.edu/science/B/people/kamaral/plasticsarticle.html" target="_blank">
                        How Plastic Endangers Ocean Life
                    </a>
                    <br/>
                    <a rel="nofollow" href="http://nsgl.gso.uri.edu/whoi/whoig11001.pdf" target="_blank">
                        How Long Trash Lasts in the Ocean
                    </a>
                    <hr/>
                </p>

                <h3>Use Smart Gardening Techniques</h3>
                <p>Many chemical products, such as pesticides and herbicides, that are commonly
                    used by gardeners are washed away into the soil and drains. These chemicals
                    eventually drain into the ocean and pollute the water. To help avoid this,
                    use natural products instead, or at least limit the use of chemicals. The same
                    principle applies to other substances, such as car washing soaps and waxes.
                </p>

                <p>
                    <a rel="nofollow" href="http://ccuh.ucdavis.edu/projects/Urban%20Run-Off%20Water%20Quality%20Research/tip-cards/QT_TopTen.pdf" target="_blank">
                        Clean Water Gardening Tips
                    </a>
                    <br/>
                    <a rel="nofollow" href="http://dpw.lacounty.gov/epd/ea/stormwater/pdf/Fertilizingtips.pdf" target="_blank">
                        Avoid Over Fertilizing
                    </a>
                    <hr/>
                </p>

                <h3>Buy Ocean Friendly Goods</h3>
                <p>Some companies are quite reckless in how they procure raw materials to manufacture
                    their products. For example, they may take pieces of coral reefs or tortoise shells.
                    This hurts the natural marine environment since these types of things slowly grow
                    over many years or even decades. To make a difference, shop at stores that use
                    ocean-friendly practices.
                </p>

                <p>
                    <a rel="nofollow" href="http://www.blueoceansociety.org/Store/giftguide.html" target="_blank">
                        Examples of Ocean-Friendly Companies
