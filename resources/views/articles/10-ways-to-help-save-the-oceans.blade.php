                <h3>Support Ocean Protection Organizations</h3>
                <p>Not all of us can join a marine conservation team out in the field, but we can
                    help out in other ways. Donating to charity and research organizations is a big
                    way to encourage their work. Alternatively, join a short term project, put up
                    posters, or ask if there are other ways to volunteer. Organizations often require
                    other types of help apart from scientists or cleanup crews. For example, they may
                    need administrative or promotional volunteers too.
                </p>

