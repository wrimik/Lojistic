                <h3>Freight Transportation by Truck</h3>

                <p>Many types of goods travel back and forth across America by truck. Clothing,
                    food, cars, tools, and heavy machinery are just a few examples of the commercial
                    goods transported by trucks. During the 1930s, business owners began to deliver
                    their goods by truck. At that time, automobiles were becoming popular and, as
                    a consequence, the roads were being paved over and improved. The introduction
                    of the Interstate Highway System in the mid-20<sup>th</sup> century allowed the
                    trucking industry to experience tremendous growth. Today, trucks are responsible
                    for a large percentage of the commercial shipping activity that occurs in the United States.
                </p>
