                <h3>Freight Transportation by Airplane</h3>

                <p>The invention of the airplane in the early twentieth century brought forth
                    another option in the realm of freight transportation. Over the years,
                    improvements were made to early airplanes that eventually lead to the development
                    of the jet. Jets and airplanes became a reliable way to transport commercial
                    goods (cargo) such as furniture, cars, and certain types of machinery. Sometimes
                    large animals such as horses are transported via airplane. Today, there are some
                    airlines that provide overnight delivery of packages and letters. Delivering freight
                    by airplane is fast and efficient though sometimes dangerous weather conditions can
                    delay or cancel a flight. Some business owners who want their goods transported in
                    the fastest way possible opt for delivery by air.
                </p>
