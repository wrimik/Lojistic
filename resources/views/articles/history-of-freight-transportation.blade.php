                <h2>History of Freight Transportation</h2>
                <p>Throughout the centuries there have been many modes of freight transportation. For many
                    years, goods traveled to their destinations by way of horse drawn wagons. This mode of
                    moving freight was slow and often dangerous. Later, with the introduction of the railway
                    system, business owners could send goods across the country in a faster, safer way. Goods
                    traveled to various cities via steamboat and, later, cargo ships delivered goods to foreign
                    countries. The invention of airplanes created another option for transporting freight to
                    faraway destinations. Business owners have always looked for better, faster, more secure
                    ways to ship their goods. The following looks at the steady progress in the history of
                    freight transportation.
                </p>

                <h3>Transporting Freight by Ship</h3>
                <p>
                    Before the introduction of the steamboat in the early nineteenth century, many
                    types of goods were moved by flatboat or raft. It was risky to move freight
                    by these basic modes of transportation as well as time-consuming. In addition,
                    flatboats, rafts, and similar watercraft had to be paddled by crew members.
                    Steamboats transported goods in a faster, more efficient way. Today, we have
                    cargo ships that are able to transport heavy machinery, pipes, huge rolls of wire,
                    and even foods packed into special refrigerated compartments. In addition,
                    there are containerships that carry cargo in large containers that can be
                    moved easily on and off a ship. These containers may house foods, electronics
                    or textiles along with a variety of other commercial goods.
                </p>
