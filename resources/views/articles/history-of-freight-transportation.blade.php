                <h3>Transporting Freight by Train</h3>

                <p>A railroad line connecting the western part of America with the eastern part was
                    in the minds of visionaries for years. A railroad line that traveled across the
                    entire country could transport a variety of commercial goods as well as passengers.
                    This idea came to life in the form of the Transcontinental Railroad. In 1869, the
                    tracks of the Union Pacific Railroad and the Central Pacific Railroad joined together
                    to create the Transcontinental Railroad. Business owners who transported their goods
                    across country by the Transcontinental Railroad system knew that their valuables would
                    be secure and arrive at their destination in a timely fashion. They no longer had to
                    depend on slow horse drawn wagons that were vulnerable to poor road conditions and other
                    mishaps. Business owners had a better, more reliable way to deliver their goods! Today,
                    freight trains can deliver large quantities of goods across the country. Some freight
                    trains are made to transport raw materials such as coal and other types of minerals.
                    Other freight trains carry goods that need to be refrigerated such as vegetables or
                    various dairy products. One of the biggest advantages of transporting goods by train is
                    that this mode of transportation doesn&rsquo;t have to compete with cars or trucks on
                    the road. It has its own set path all the way through to its destination!
                </p>
