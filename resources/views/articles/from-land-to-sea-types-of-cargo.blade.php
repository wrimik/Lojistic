                    <h3>Dangerous or Hazardous Cargo</h3>

                    <p>Carriers and handlers must pay close attention to freight that contains hazardous or dangerous
                        materials. Freight that contains these hazardous materials must undergo rigorous inspection, and
                        typically requires the carriers andhandlers to follow strict safety protocol. In some instances,
                        carriers and handlers may need to wear safety equipment to ensure that it does not pose severe
                        harm to them on human contact. Carriers and handlers constitute hazardous materials as harsh
                        chemicals, oil, asbestos, petrol, diesel, and other undisclosed agents. Carriers and handlers
                        must exercise extreme caution when transporting these items to avoid accidents and potential
                        fatalities.</p>
                    <li><a href="http://library.enlisted.info/field-manuals/series-2/FM21_305/CH19.PDF" rel="nofollow">Dangerous
                            and Hazardous Cargo and Materials (PDF)</a></li>
                    <br/>

                    <h3>Inflammable or Combustible Cargo</h3>

                    <p>Carriers and handlers may also come in contact with inflammable and combustible cargo. Freight
                        that contains inflammable and combustible cargo must undergo rigorous inspection, and usually
                        entails the strict adherence to safety protocol. Inflammable and combustible cargomay
                        spontaneously ignite due to the nature of the caustic materials contained within the freight. In
                        addition, each freight container can easily catch on fire, thus posing the risk of setting the
                        entire shipment ablaze. Carriers and handlers must wear safety equipment when handling this type
                        of cargo as it may pose a risk of serious injury and fatality. Inflammable and combustible cargo
                        may include goods, such as oil seeds, diesel, cotton, oil, and jute.</p>
                    <li>
