                    <h3>Bulk Cargo </h3>

                    <p>Carriers and handlers prepare and deliver scrap metal, grains, gypsum, logs, salt, aluminum, wood
                        chips, and sand in bulk. Freight containing these items requires special handling to ensure that
                        the shipment does not become ruined by the elements. Some of these items will need extra
                        protection from moisture exposure. As with other shipments, carriers and handlers should
                        exercise safety precautions when preparing bulk items, especially heavy metals with sharp and
                        rigged edges. Most transportation and distribution centers administer theirown safety protocol
                        in accordance with federal, state, and local regulations.</p>
                    <li><a href="http://www.standard-club.com/docs/15056CargoJan2011Bulletinv06.pdf" rel="nofollow">Bulk
                            Cargoes: Hold Preparation and Cleaning (PDF)</a></li>
                    <br/>

                    <h3>Food Products </h3>

                    <p>Carriers and handlers receive shipments that contain perishable and non-perishable foods, such as
                        grains, fresh produce, jellies, jams, pickles, syrups, canned vegetables, tinned fruit, frozen
                        fruits and vegetables, sea food, frozen meat, and beer. Carriers and handlers who prepare and
                        deliver food products must ensure that the transportation vessel has a refrigerated storage unit
                        for perishable items. The temperature and moisture levels of the refrigerated storage unit must
                        adhere to federal, state, and local regulations. In addition, the food products stored within
                        the refrigerated unit must not exceed its maximum capacity. Carriers and handlers who follow
                        these regulatory standards will minimize any potential loss.</p>
                    <li><a href="http://blog.udot.utah.gov/wp-content/uploads/2011/09/refrigerated-freight-6_6_11.pdf"
                           rel="nofollow">Refrigerated Freight in Utah (PDF)</a></li>
                    <br/>

                    <h3>Military Equipment and Project Cargo </h3>

                    <p>The military often issues special deliveries of large equipment that cannot be fit into any
                        containers, such as windmills, air conditioners, generators, and defense weapons. Carriers and
                        handlers who prepare and deliver these items should follow the orders given by the military
                        personnel administering the shipment. Due to the size of these items, carriers and handlers
                        should restrain the equipment with the proper tools and materials. In addition, carriers and
                        handlers should wear the proper safety equipment to prevent potential injuries</p>
                    <li><a href="http://www.hnsa.org/doc/pdf/fm55_17_army_cargo.pdf" rel="nofollow">Cargo Specialists'
                            Handbook (PDF)</a></li>
                    <br/>

                    <h3>Leather Hides and Textiles </h3>

                    <p>Carriers and handlers may have to prepare and deliver leather hides to clothing, apparel, and
                        related manufacturers. Freight containing products made from leather hides often produce strong
                        odors. This requires the transportation carrier to keep the freight in isolation, especially
                        when handlers have prepared the leather hides with salt. Carriers and handlers should wear
                        respirators when handling leather hides as they tend to emit noxious fumes.</p>
                    <li><a href="http://www.tis-gdv.de/tis_e/ware/textil/textilien/textilien.htm" rel="nofollow">Textiles,
                            Fabric, and Cloth</a></li>
                    <br/>

                    <h3>Fragile Items </h3>

                    <p>Every freight shipment requires transportation carriers to handle the shipment with care;
                        however, some cargo contains fragile items that require extra caution. Fragile items may include
                        items, such as clay figurines, glassware, chinaware, small machinery, tools, furniture, lamps,
                        computers, televisions, and electronic gadgets. Handlers must package these items with extreme
                        care. In addition, the transportation vessel should have added protection to prevent incurring
                        damage. Carriers and handlers should wear safety equipment in the event these items shatter,
                        tear, burst, or otherwise become ruined.</p>
                    <li><a href="http://www.worldshipping.org/pdf/industry_guidance_shippers_container_stuffers.pdf"
