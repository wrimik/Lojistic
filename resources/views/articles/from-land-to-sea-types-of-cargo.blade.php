                    <h3>Concentrates or Ores </h3>

                    <p>Carriers and handlers receive orders to prepare and deliver cargo containers with concentrates or
                        ores within them. This type of freight is extremely dense. In addition, freight containing
                        concentrates may emit toxic fumes, which can deplete oxygen inside of the tank. Therefore,
                        handlers and carriers must exercise caution and wear respirators when handling concentrates and
                        ores. Carriers and handlers must enact preventative measures to ensure that the concentrates and
                        ores do not absorb excessive moisture.</p>
                    <li>
                        <a href="http://www.ukpandi.com/fileadmin/uploads/uk-pi/LP%20Documents/Carefully_to_Carry/Mineral%20ore%20concentrates%20and%20other%20materials%20which%20may%20liquefy.pdf"
                           rel="nofollow">Mineral Ore Concentrates and Other Materials That May Liquefy</a></li>
