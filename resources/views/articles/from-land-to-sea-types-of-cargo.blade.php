                <h2>From Land To Sea; Types of Cargo</h2>

                <p>In the private sector, small businesses and corporations need to have their goods transported in bulk
                    to a distribution center. Any designated shipment uses one of several transportation mediums,
                    including marine, air, train, and road carriers. Freight carriers may use numerous transportation
                    vessels, including container ships, airplanes, box cars, and tractor trailers to deliver cargo to
                    its assigned port. Commercial establishments refer to these goods as cargo, or freight, and consider
                    these packaged items as high-value assets. Each shipment may be transported through the air or
                    water. The exchange of cargo between carriers occurs at terminals, wherein handlers prepare the next
                    shipment for delivery to its corresponding distribution center.</p>

                <h3>Heavy Equipment and Machinery</h3>

                <p>Industrial cargo usually includes household machinery, technical machinery, automobiles, auxiliary
                    machinery, and engineering machinery. Handlers move heavy equipment and machinery onto wooden
                    crates, or conceal them in heavy-duty steel containers. Handlers must follow strict instructions
                    given to them by the client. In addition, seaport and airport terminals must investigate each
                    package to ensure that it has not incurred damage during transit. Carriers and handlers must also
                    follow regulations imposed onto them by federal, state, and local agencies.
                </p>
