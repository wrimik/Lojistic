                <h3>Model Train Producers, Museums, and Exhibits</h3>

                <p>Hundreds of model train manufacturers provide scores of
                    products for enthusiasts. Bachmann Industries, one of the
                    oldest model train manufacturers, have remained as one of
                    the leaders in the industry since 1833. Lionel developed and
                    maintained its legacy as a leading model train producer since
                    1900. Other recognizable manufacturers include USA Trains,
                    Atlas O Trains, Model Rectifier Corporation (MRC), and the
                    Rokenbok Toy Company. Over the years, enthusiasts have formed
                    organizations, museums, and exhibits to help support the hobby
                    of model railroading. This has kept the hobby alive, while
                    also keeping the model railroading market rejuvenated for
                    future generations.</p>
