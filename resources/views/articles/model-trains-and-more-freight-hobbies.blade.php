                <h2>Model Trains and More Freight Hobbies!</h2>

                <p>Model railroading has existed as a hobby for over a century. It
                    combines radio-controlled technology with novelty railway cars to
                    form a dynamic hobby. Model train enthusiasts take pleasure in
                    constructing train layouts, buildings, and scenery. No two hobbyists
                    perceive model railroading in the same light. Therefore, beginners
                    should become familiar with the basics before pursuing more advanced
                    concepts. Understanding the history of the hobby and its founders
                    will help put into perspective the appreciation people have for it.
                    In addition, some basic understanding about the scale of a model
                    train and the gauge of a train track helps when it comes time to
                    lay down the track.</p>

                <h3>The History of Model Trains as a Hobby</h3>

                <p>The
                    history of model trains traces back as far as actual railroads
                    themselves. The earliest model trains were made from metal
                    and powered by steam or clockwork components. The average
                    home did not have electricity to power model trains in the
                    same way that modern homes do. As a result, the earliest
                    model trains had high price tags that only the wealthy could
                    afford. As the prices began to drop in conjunction with the
                    improvements involved with model railroading, more enthusiasts
                    started to get involved with the hobby. Model railroads started
                    to become more common from the 1920s as they were mass-produced
                    for the general public.</p>
