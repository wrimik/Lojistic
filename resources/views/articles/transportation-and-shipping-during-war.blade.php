                <h3>Vietnam War (1955 – 1975) – 20<sup>th</sup> Century</h3>

                <p>Airlift missions were flown into French Indochina by the Military Air Transport System (MATS) in
                    1948. MATS provided military supplies and equipment to the colonial Vietnamese forces and French
                    government who were fighting the Viet Minh. The wounded from Dien Bien Phu were transported to
                    France or Algeria from the Tan Son Nhut Airport. C-124s flew wounded soldiers from Saigon and
                    were then airlifted to the United States using MATS C-97s. MATS airlift flights were used to
                    ship military aid to South Vietnam during the 1950s and the early 1960s. Equipment and personnel
                    being transported to war zones was done mostly by military air transport.</p>
