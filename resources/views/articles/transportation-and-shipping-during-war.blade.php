                <h3>World War I (1914 – 1918) – 20<sup>th</sup> Century</h3>

                <p>The United States wasn&rsquo;t quite prepared to transport necessary equipment and soldiers
                    during World War I. Transport ships were needed to transport United States troops to European
                    countries so service cruise ships were used, German ships were seized, and Allied ships were
                    borrowed to transport the soldiers from Virginia, New York, and New Jersey. The American mi
                    litary limits were taxed and strategies for command structures and organization were in order to
                    transport large numbers of supplies and troops efficiently and quickly. The wounded were also
                    transported by the very first motorized ambulances during this war.</p>
