                <h3>World War II (1939 – 1945) – 20<sup>th</sup> Century</h3>

                <p>During World War II there were more materials and people than before and travel was mainly done
                    by railroad. There was an increase in demand and steam railroads carried around 62 percent of
                    the freight. By 1944 this amount almost doubled. The war delayed converting steam engines to
                    diesel locomotives. Although the Army had their own ships, government merchant ships transported
                    aviation fuel, trucks, tanks, explosives, ammunition, landing craft, airplanes, medicines, food,
                    and locomotives. Raw material used for making ammunition, tanks, and airplanes were also
                    transported with merchant ships.</p>
