                <h2>Transportation and Shipping During War</h2>

                <p>In the 18<sup>th</sup> century river and water transportation mostly took place by sailing a vessel
                    whereas traveling on land took place by horse. The late 18<sup>th</sup> century, and early
                    19<sup>th</sup> century, saw an economic expansion, which spurred the building of canals in order to
                    speed up the delivery of goods to the market. As the southern and mid-western states developed, the
                    Mississippi River system was a quicker way to transport goods and people as steamboats were
                    introduced. The steamboats provided freight and passenger transportation until railroads were
                    developed later in the 19<sup>th</sup> century which reduced the presence of steamboats. The
                    railroad expansion provided a year round and quick means of transportation which ended the canal
                    boom. The pipeline transport of natural gas and petroleum was expanded greatly in the
                    20<sup>th</sup> century due to wartime expediency and the majority of domestic long haul was taken
                    over.</p>

                <p>The personal doctor of Napoleon had concerns about how wounded soldiers were being transported back
                    from battle. They were taken away in an open cart and being jostled over rough ground so Baron
                    Larrey invented a more comfortable “ambulance” in 1792. There were twelve ambulances being used by a
                    French Army unit in 1796 but it would be over 80 years before civilians used them. In 1895, in
                    Paris, an ambulance with an engine was demonstrated but was not accepted until 1900 by the French
                    Army. The very first civilian ambulance with its own motor would be used in France the same year.
                    This motorized ambulance would not be used for wounded soldiers until World War I. Up until then,
                    wounded soldiers were taken away in the open carts and the majority of transportation during the war
                    was done on foot, on horse, or with horse and buggy.</p>

                <h3>The Civil War (1861 – 1865) – 19<sup>th</sup> Century</h3>

                <p>During the Civil War, most transportation was on horseback or consisted of infantry marches.
                    Cooking gear, cannons and other larger supplies and equipment was moved using wagons and horses.
                    Trains and ships were used during this time as well. The steamboat and steam engine moved larger
                    equipment and troops long distances. Most infantry did their traveling on foot; however horses
                    were used a lot in moving supplies and equipment. Smaller cavalry groups rode horses and were
                    used to attack quickly in the enemy&rsquo;s territory. Army Corp engineers built bridges and
                    roads for troop transportation and Union Corp engineers built bridges and laid the tracks for
                    trains used in the military.</p>
