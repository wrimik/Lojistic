                <h2>Dollars for Your Parcel - The History of Bartering</h2>
            <p>
                Bartering involves exchanging goods and services for other items of an
                equal or desirable value. Barter usually occurs in societies or social
                situations that do not rely on a medium of exchange, such as money, to
                conduct a business transaction. The rate of bartering usually increases
                during times of an unstable financial crisis that has significantly
                devalued a nation's currency, especially during a deflationary spiral.
                In addition, many societies who have suffered hyperinflation may result
                to bartering goods and services. Bartering does occur sparingly in
                developed and underdeveloped countries during stable economic times.
            </p>
            <h3>History</h3>
            <p>
                No historical evidence supports the common misconception that entire societies
                thrived off trade and barter. In fact, pre-monetary societies relied largely
                on the core principles of gift economics and debt. Bartering usually only
                occurred between total strangers or suspected enemies.
            </p>
            <p>
                Modern bartering  efforts usually occur on an informal basis between private individuals
                and small businesses; however, attempts at organizing larger-scale barter
                exchanges have been conducted by thirty parties. Bartering provides several
                benefits for companies, corporations, and even entire countries, such as
                Brazil, Thailand, and the Soviet Union. For instance, it allows companies to
                engage in the exchange of goods and services without a medium of exchange,
                while enabling others to conduct business as usual. In addition, it enables
                companies who lack a medium of exchange to obtain goods and services from other
                bartering participants.</p>
                <h3>Trade Exchanges</h3>
                <p>
                    A barter exchange operates in the same vein as brokers and banks. For
                    instance, it provides trading platform and bookkeeping abilities as
                    monetary systems. The barter exchange acts as the median between each
                    business transaction by debiting a participating member's account after
                    each purchase, and crediting the account when member makes a sale.
                </p>
                <p>
                    Barter exchanges eliminate the risk of unequal exchanges between the two parties
                    that would normally occur on an individual basis. Barter exchanges also
                    provide effective methods of increasing profit, decreasing expenses, and
                    transporting inventory. Businesses enrolled in a barter exchange earn credits,
                    instead of currency, in their accounts where they can conduct further
                    exchanges with other businesses. Barter exchanges charge a percentage of
                    each sale, and may enforce applicable fees for both the buying and selling
                    parties.</p>
                <h3>Barter Markets</h3>
            <p>
                Barter markets exist in areas where groups of individuals meet to exchange
                unneeded items for another individual's unwanted goods and services.
                Barter markets have exponentially grown in the Catalonia region of Spain,
                where they resemble the exchanges that occur at swap meets. In other words,
                participants swap their goods and services until finding the objects each
                party desires. Multiple parties often help satisfy the tastes of participants
                in a bartering system that attempts to get around using currency as a medium
                of exchange.
            </p>
            <h3>Environmental Implications</h3>
            <p>Corporate bartering may drastically reduce the need to manufacture goods
                in a dismal economy. In addition, it helps contribute towards a cleaner
                environment, because corporate bodies tend to focus on ridding of its
                current inventory, rather than stocking it shelves with newer products.
            </p>
            <p>
                In other words, a global marketplace focused on bartering mitigates waste
                and transitions its position towards a productive economy. A number of
                environmentally friendly organizations have campaigned to raise corporate
                awareness about the significance bartering can have on the planet and its
                inhabitants.
            </p>
            <h3>Limitations of a Barter Economy</h3>
            <p>Despite its advantages, a barter economy does pose several limitations for
                individuals, small businesses, and corporate bodies. For instance, bartering
                only works when two parties demand the goods and services that the otherparty
                wants or currently posses. A monetary system also provides a measurement of
                value for all existing goods and services, a function that does not exist in
                a bartering economy.
            </p>
            <p>
                In addition, buyers can not buy a certain amount of another
                individual's goods or services, which makes it impossible to divide valuable
                goods that another individual could obtain in a currency-based system.
                A barter economy lacks standards for deferred payments, because of its absence
                of measured value; however, trade exchanges make this possible if the overseer
                denominates the debt in units. Bartering also poses certain difficulties for
                storing perishable goods for the accumulation of wealth, which makes future
                planning impractical.
            </p>

